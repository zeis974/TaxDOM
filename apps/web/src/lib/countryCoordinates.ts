/**
 * Calcule le point milieu entre deux coordonnées [lat, lon]
 * pour centrer la caméra sur une vue qui englobe les deux points.
 */
export function getMidpointCoordinates(a: Coordinates, b: Coordinates): Coordinates {
  const latA = (a[0] * Math.PI) / 180
  const lonA = (a[1] * Math.PI) / 180
  const latB = (b[0] * Math.PI) / 180
  const lonB = (b[1] * Math.PI) / 180

  const dLon = lonB - lonA

  const Bx = Math.cos(latB) * Math.cos(dLon)
  const By = Math.cos(latB) * Math.sin(dLon)

  const latMid = Math.atan2(
    Math.sin(latA) + Math.sin(latB),
    Math.sqrt((Math.cos(latA) + Bx) * (Math.cos(latA) + Bx) + By * By),
  )
  const lonMid = lonA + Math.atan2(By, Math.cos(latA) + Bx)

  return [(latMid * 180) / Math.PI, (lonMid * 180) / Math.PI]
}

/**
 * Calcule la distance angulaire (en degrés) entre deux coordonnées.
 */
export function getAngularDistance(a: Coordinates, b: Coordinates): number {
  const latA = (a[0] * Math.PI) / 180
  const lonA = (a[1] * Math.PI) / 180
  const latB = (b[0] * Math.PI) / 180
  const lonB = (b[1] * Math.PI) / 180

  const dLat = latB - latA
  const dLon = lonB - lonA

  const sinDLat = Math.sin(dLat / 2)
  const sinDLon = Math.sin(dLon / 2)

  const c =
    2 *
    Math.asin(Math.sqrt(sinDLat * sinDLat + Math.cos(latA) * Math.cos(latB) * sinDLon * sinDLon))

  return (c * 180) / Math.PI
}

// ── Fonctions utilitaires vectorielles 3D ──

function latLonToVector([lat, lon]: Coordinates): [number, number, number] {
  const latRad = (lat * Math.PI) / 180
  const lonRad = (lon * Math.PI) / 180
  return [
    Math.cos(latRad) * Math.cos(lonRad),
    Math.cos(latRad) * Math.sin(lonRad),
    Math.sin(latRad),
  ]
}

function vectorToLatLon([x, y, z]: [number, number, number]): Coordinates {
  return [
    (Math.atan2(z, Math.sqrt(x * x + y * y)) * 180) / Math.PI,
    (Math.atan2(y, x) * 180) / Math.PI,
  ]
}

function normalizeVector(v: [number, number, number]): [number, number, number] {
  const len = Math.sqrt(v[0] * v[0] + v[1] * v[1] + v[2] * v[2])
  if (len === 0) return [0, 0, 1]
  return [v[0] / len, v[1] / len, v[2] / len]
}

function crossProduct(
  a: [number, number, number],
  b: [number, number, number],
): [number, number, number] {
  return [a[1] * b[2] - a[2] * b[1], a[2] * b[0] - a[0] * b[2], a[0] * b[1] - a[1] * b[0]]
}

function dotProduct(a: [number, number, number], b: [number, number, number]): number {
  return a[0] * b[0] + a[1] * b[1] + a[2] * b[2]
}

/**
 * Interpolation sphérique (SLERP) entre deux coordonnées.
 * t = 0 → a, t = 1 → b
 */
export function slerpCoordinates(a: Coordinates, b: Coordinates, t: number): Coordinates {
  const ua = latLonToVector(a)
  const ub = latLonToVector(b)

  const dot = Math.min(1, Math.max(-1, dotProduct(ua, ub)))
  const theta = Math.acos(Math.abs(dot))

  let result: [number, number, number]

  if (theta < 0.001) {
    // Points quasi-identiques : interpolation linéaire
    result = [ua[0] + t * (ub[0] - ua[0]), ua[1] + t * (ub[1] - ua[1]), ua[2] + t * (ub[2] - ua[2])]
  } else {
    const sinTheta = Math.sin(theta)
    const wA = Math.sin((1 - t) * theta) / sinTheta
    const wB = Math.sin(t * theta) / sinTheta
    result = [ua[0] * wA + ub[0] * wB, ua[1] * wA + ub[1] * wB, ua[2] * wA + ub[2] * wB]
  }

  return vectorToLatLon(normalizeVector(result))
}

/**
 * Calcule le meilleur point de vue pour voir un arc entre deux coordonnées.
 *
 * L'angle de bascule est adapté à la longueur de l'arc pour garantir que
 * les deux extrémités restent visibles (< 90° du centre caméra).
 * Le côté du pôle est choisi en faveur de la vue depuis l'Europe (lat 48°N, lon 10°E).
 */
export function getArcViewpoint(a: Coordinates, b: Coordinates): Coordinates {
  const ua = latLonToVector(a)
  const ub = latLonToVector(b)

  // Midpoint vectoriel (somme normalisée)
  const um = normalizeVector([ua[0] + ub[0], ua[1] + ub[1], ua[2] + ub[2]])

  // Pôle du grand cercle (perpendiculaire au plan de l'arc)
  const up = normalizeVector(crossProduct(ua, ub))
  const upNeg: [number, number, number] = [-up[0], -up[1], -up[2]]

  // Référence géographique réelle de l'Europe centrale
  const eu = latLonToVector([48, 10])

  // Angle de bascule adaptatif : limité pour que les extrémités restent dans
  // le demi-sphère visible (marge de 10° par rapport à l'horizon).
  const halfArc = getAngularDistance(a, b) / 2
  const maxSafeTilt = Math.max(0, 90 - halfArc - 10)
  const tiltDeg = Math.min(40, maxSafeTilt)
  const tilt = (tiltDeg * Math.PI) / 180
  const cosT = Math.cos(tilt)
  const sinT = Math.sin(tilt)

  const v1: [number, number, number] = [
    um[0] * cosT + up[0] * sinT,
    um[1] * cosT + up[1] * sinT,
    um[2] * cosT + up[2] * sinT,
  ]

  const v2: [number, number, number] = [
    um[0] * cosT + upNeg[0] * sinT,
    um[1] * cosT + upNeg[1] * sinT,
    um[2] * cosT + upNeg[2] * sinT,
  ]

  // Choisir le côté le plus proche de la vue Europe
  const dot1 = dotProduct(normalizeVector(v1), eu)
  const dot2 = dotProduct(normalizeVector(v2), eu)

  const best = dot1 >= dot2 ? normalizeVector(v1) : normalizeVector(v2)
  return vectorToLatLon(best)
}
