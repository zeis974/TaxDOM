/**
 * Convertit des coordonnées [lat, lon] en angles Cobe [phi, theta]
 * Formule officielle de cobe.vercel.app
 */
export function coordsToCobeAngles([lat, lon]: Coordinates): { phi: number; theta: number } {
  return {
    theta: (lat * Math.PI) / 180,
    phi: Math.PI - ((lon * Math.PI) / 180 - Math.PI / 2),
  }
}

